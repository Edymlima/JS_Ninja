/* Operadores de comparacao
    >     maior
    <     menor
    >=    maior que
    <=    menor que
    !=    diferente
    ==    igual
    ===
*/  

1 > 2
// false
1 < 2
// true
1 != 2
// true
2 == 2
// true
"Z" < "a"
// true

/* Operadores de comparacao boolean */

true && false
// false
true && true
// true
true && true && false
// false

// true
true || true
// true
false || false
// false
true || false || false
// true

!true
// false
!false
// true

/* Conversao para String */
String(2) + 2
// "22"

/* Conversao para Number */
Number("5") + 2
// 7

/* Conversao boolean para Number */
Number(true)
// 1

/* Conversao para Number */
Number("2") + Number("1")
// 3